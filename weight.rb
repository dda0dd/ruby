# 変数展開
name = "A" #文字列型
weight = 50 #整数型

puts name + "さんの体重は" + weight.to_s + "kgです" #to_s用いて文字列型に変換（＋による連結可能になる）
puts "#{name}さんの体重は#{weight}kgです" #"{変数}"と記述（変数展開コード）
#変数展開用いて、元の変数がどの型でも文字列型に変換してくれる

puts "#{name}さんの体重は#{weight * 2}kgです" #変数を使った演算結果展開もできる

puts '#{name}さんの体重は#{weight}kgです' 
#シングルクォーテーションで囲むと文字列になるので注意
